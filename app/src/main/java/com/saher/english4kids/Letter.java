package com.saher.english4kids;

public class Letter {
    String bl,sl;

    public Letter(String bl, String sl) {
        this.bl = bl;
        this.sl = sl;
    }

    public String getBl() {
        return bl;
    }

    public void setBl(String bl) {
        this.bl = bl;
    }

    public String getSl() {
        return sl;
    }

    public void setSl(String sl) {
        this.sl = sl;
    }
}
